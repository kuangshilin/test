# 常量
注射器尺寸 = [1, 3, 5]  # ml
剂量选项 = [(0.8, 1), (2, 3), (3, 3), (4, 5), (5, 5)]  # (实际体积, 注射器容量)
注射次数 = range(10, 181, 10)  # 从10到180，步长为10

# 计算单次剂量的函数
def 计算单次剂量(实际体积, 注射器容量, 注射次数):
    return 实际体积 / 注射次数

# 生成组合并计算单次剂量
组合 = []
for 尺寸 in 注射器尺寸:
    for 选项 in 剂量选项:
        for 次数 in 注射次数:
            实际体积, 注射器容量 = 选项
            单次剂量 = 计算单次剂量(实际体积, 注射器容量, 次数)
            组合.append((尺寸, 选项, 次数, 单次剂量))

# 输出组合和单次剂量
for 组合 in 组合:
    尺寸, 选项, 次数, 单次剂量 = 组合
    print(f"注射器尺寸: {尺寸}ml, 剂量选项: {选项}, 注射次数: {次数}, 单次剂量: {单次剂量}ml")
